--  DDL for Procedure SP_PTL_UPDATE_DOCUMENT_STATUS
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "SP_PTL"."SP_PTL_UPDATE_DOCUMENT_STATUS" 
   (
    
    SP_COM_DOC_ID   in NUMBER,           -- ΚΩΔΙΚΌΣ ΕΓΓΡΑΦΟΥ 
    APPLICATION_TYPE_NUMBER in NUMBER,   -- ΚΩΔΙΚΌΣ ΥΠΗΡΕΣΙΑΣ 
    -- 1 Εργατική Καταγγελία
    -- 2 Εργατική Διαφορεα 
    -- 3 .....
    APPLICATION_STATUS_NUMBER in NUMBER, -- ΚΑΤΑΣΤΑΣΗ ΥΠΟΒΟΛΉΣ
    -- UNKNOWN("UNKNOWN", 0),
    -- INPROGRESS("Σε Εξέλιξη", 1),
    -- CHARGED("Χρεώθηκε", 2),
    -- EXAMINATION("Έλεγχος", 3),
    -- RECORDS("Αρχείο", 4);
    CASE_ID_NUMBER in NUMBER,            -- ΚΩΔΙΚΌΣ ΥΠΟΘΕΣΗΣ
    -- OUTPUT PARAMETERS
    out_errMsg OUT VARCHAR2              -- ΕΠΙΣΤΡΟΦΗ ΜΉΝΥΜΑΤΟΣ ΛΑΘΟΥΣ
   )
   IS
  
   --DECLARATIONS
   AFFECTED_COLUMNS NUMBER;
   EXCEPTION_INVALID_INPUT EXCEPTION;
   VAR_TABLE_NAME VARCHAR2(30);
   VAR_SQL_STATEMENT VARCHAR2(300);
   
   BEGIN
-- Purpose: Ενημέρωση της κατάστασησς στο πορταλ μιας υποβολής 



    if SP_COM_DOC_ID is not NULL AND APPLICATION_TYPE_NUMBER is not NULL AND APPLICATION_STATUS_NUMBER is not NULL AND APPLICATION_STATUS_NUMBER < 5 then
      --------------------------------------------------------------------------
      -- MATCH APPLICATION TYPE
      --------------------------------------------------------------------------
      SELECT TABLE_NAME
      INTO VAR_TABLE_NAME
      FROM SP_PTL_REQUEST_MAP
      WHERE ID = APPLICATION_TYPE_NUMBER;
      
      -- Will throw [ORA-01403: no data found] if no match was found
      
      DBMS_OUTPUT.put_line ('MATCHED TABLE IS ' || VAR_TABLE_NAME);
      
      --------------------------------------------------------------------------
      -- BUILD DYNAMIC QUERY
      --------------------------------------------------------------------------
      if CASE_ID_NUMBER is not NULL then 
        select 'UPDATE ' || VAR_TABLE_NAME  || ' ' ||
               'SET '    || VAR_TABLE_NAME  || '.REQ_STATUS = :1, ' ||  VAR_TABLE_NAME || '.CASE_ID = :2 ' || 
               'WHERE '  ||  VAR_TABLE_NAME || '.DOC_ID = :3'  into VAR_SQL_STATEMENT from dual;
               
        -- Excecute
        --DBMS_OUTPUT.put_line (VAR_SQL_STATEMENT);
        execute immediate VAR_SQL_STATEMENT USING APPLICATION_STATUS_NUMBER, CASE_ID_NUMBER, SP_COM_DOC_ID;
      else
        select 'UPDATE ' || VAR_TABLE_NAME  || ' ' ||
               'SET '    || VAR_TABLE_NAME  || '.REQ_STATUS = :1 ' || 
               'WHERE '  ||  VAR_TABLE_NAME || '.DOC_ID = :2'  into VAR_SQL_STATEMENT from dual;
               
        -- Excecute       
        --DBMS_OUTPUT.put_line (VAR_SQL_STATEMENT);
        execute immediate VAR_SQL_STATEMENT USING APPLICATION_STATUS_NUMBER, SP_COM_DOC_ID;
      end if;
      
      --------------------------------------------------------------------------
      -- CHECK THAT AT LEAST ONE COLUMN WAS AFFECTED
      --------------------------------------------------------------------------
      AFFECTED_COLUMNS := sql%rowcount;
        
      if AFFECTED_COLUMNS = 0 then
          -- The DOC_ID was wrong, no columns were affected
          RAISE EXCEPTION_INVALID_INPUT;
      else
          commit;
      end if;
      
    else
       -- DBMS_OUTPUT.put_line ('Invalid args 2');
        RAISE EXCEPTION_INVALID_INPUT;
    end if;
  
  
    --------------------------------------------------------------------------
    -- DONE!
    --------------------------------------------------------------------------
    out_errMsg :='OK';
    DBMS_OUTPUT.put_line (out_errMsg);

EXCEPTION
    WHEN OTHERS

    THEN
        out_errMsg := 'Error occured: ' || SQLCODE || ' - ' || SQLERRM;
        DBMS_OUTPUT.put_line (out_errMsg);
        ROLLBACK;


END; -- Procedure

