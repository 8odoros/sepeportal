--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "SP_PTL"."SETDATAFILE" 
(
  USER_ID IN NUMBER 
, DOCUMENT_ID IN NUMBER 
, DOCUMENT_FILE IN BLOB 
, DOCUMENT_ID_OUT OUT NUMBER 
, out_errMsg OUT VARCHAR2 
) AS 
 
   e EXCEPTION;
   flag1 number:=0;
   flag2 number:=0;
   maxid number:=0;

BEGIN
 
if (USER_ID is null OR  DOCUMENT_FILE is null) then
  out_errMsg:='Null USER_ID of DOCUMENT FILE';
  DBMS_OUTPUT.PUT_LINE('>exception');
  RAISE e ;
end if;

select count(*) into flag1 from SP_PTL_DOCUMENT_INDEX where DOCUMENTID=DOCUMENT_ID;
select count(*) into flag2 from SP_PTL_DOCUMENT_INDEX where DOCUMENTID=DOCUMENT_ID AND USERID=USER_ID;

if (DOCUMENT_ID is null OR flag1=0) then 
  Select max(DOCUMENTID) into maxid from SP_PTL_DOCUMENT_INDEX;
  DOCUMENT_ID_OUT:=maxid+1;
  Insert into SP_PTL_DOCUMENT_INDEX VALUES(USER_ID,DOCUMENT_ID_OUT, DOCUMENT_FILE );
else
    DBMS_OUTPUT.PUT_LINE('flag1 ' ||flag1 ||' flag2 '  ||flag2);
    
  if (flag1<> flag2) then 
   out_errMsg := 'Document Exists from other user';
   DBMS_OUTPUT.PUT_LINE('exception' ||out_errMsg);
    RAISE e ;
  else
    DBMS_OUTPUT.PUT_LINE('update');
    update SP_PTL_DOCUMENT_INDEX set DATAFILE=DOCUMENT_FILE where USERID=USER_ID AND DOCUMENTID=DOCUMENT_ID ;
    DOCUMENT_ID_OUT:=DOCUMENT_ID;
  end if;
  end if;  
  
EXCEPTION
  
    WHEN OTHERS  THEN
        out_errMsg := 'Error occured: ' || SQLCODE || ' - ' || out_errMsg;
        DBMS_OUTPUT.put_line ('Error occured: ' || SQLCODE || ' - ' || out_errMsg);
        ROLLBACK;
END  ;

